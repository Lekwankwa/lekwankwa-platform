def get_fx(currency: str, year: int) -> float:
    """
    Return the period-appropriate annual average exchange rate.
    Falls back to the closest available year, then to 1.0.
    """
    rates = HISTORICAL_FX.get(currency)
    if not rates:
        return 1.0
    if year in rates:
        return rates[year]
    # Find closest year
    available = sorted(rates.keys())
    if year < available[0]:
        return rates[available[0]]
    if year > available[-1]:
        return rates[available[-1]]
    # Linear interpolation between flanking years
    lower = max(y for y in available if y <= year)
    upper = min(y for y in available if y >= year)
    if lower == upper:
        return rates[lower]
    frac = (year - lower) / (upper - lower)
    return round(rates[lower] + frac * (rates[upper] - rates[lower]), 4)


